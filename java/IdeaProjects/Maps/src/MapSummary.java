    /*

        A Map is like a Collection in many ways, but it is not "officially" part of the
        Collections Framework.

        Maps store their elements in key->value pairs. They also have certain restrictions
        on how their keys and values can be accessed. Maps may only contain unique keys.
        Some implementations of Map allow for a single null key.

        The three most common implementations of Map are the HashMap, the LinkedHashMap,
        and the TreeMap. The HashTable is a legacy class retro-fitted into the Map
        framework. Its implementation is essentially the same as HashMap, and as such
        is not a good choice due to its complexity and overhead. We will not cover it
        in these documents.

    **/

