public class DataTypes {
    public static void main(String[] args) {
        System.out.println("Variable overview...");

        int myFirstNumber = 5;
        int mySecondNumber = 6;
        int myThirdNumber = 7;
        System.out.println(myFirstNumber);

        int myFirstExpression = 3 * 7;
        System.out.println(myFirstExpression);

        int myTotal = myFirstNumber + mySecondNumber + myThirdNumber;
        System.out.println("The total of my three variables is " + myTotal);

    }
}
